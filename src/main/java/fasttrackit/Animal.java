package fasttrackit;

public class Animal {
    String animalName;
    int animalAge;
    int animalHealth;
    int animalHunger;
    int animalMood;
    String favoriteFood;
    String favoriteActivity;

    public Animal (String name, int age, int health, int hunger, int mood, String food, String activity)
    {
        animalName = name;
        animalAge = age;
        animalHealth = health;
        animalHunger = hunger;
        animalMood = mood;
        favoriteFood = food;
        favoriteActivity = activity;
    }


}
